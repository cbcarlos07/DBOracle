 private void assinar(){
     setCursor(Cursor.getPredefinedCursor(3));
     pc = new Prescricao_Controller();
     boolean teste = pc.assinar_prescricao(Long.parseLong(jt_codigo.getText()));
     if(teste){
         JOptionPane.showMessageDialog(null, "Prescrição assinada com sucesso");
     }
     setCursor(Cursor.getPredefinedCursor(0));
 }
    private void deletar_itens(){
        int opcao = JOptionPane.showConfirmDialog(null,"Tem certeza de que deseja o(s) item(ns) da prescrição?", "Confirmação", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
               
       if(opcao == JOptionPane.YES_OPTION){
            pc = new Prescricao_Controller();
                boolean teste = pc.excluir_itens(Long.parseLong(jt_codigo.getText()));
                if(teste){
                    DefaultTableModel tabela = (DefaultTableModel) table.getModel();
                    tabela.setNumRows(0);
                }
       }
       
    }
    
    private void buscar(){
       pc = new Prescricao_Controller();
      Prescricao p = new Prescricao();
      p = pc.busca(Integer.parseInt(jt_codigo.getText()));
      //try{
        try{    
            jt_nome.setText(p.getPaciente().getNome());
            jt_prestador.setText(p.getPrestador().getPrestador());
            jt_local.setText(p.getLocal());
            jd_data.setDate(p.getData());
            String fechado = p.getFechado();
            System.out.println("Fechado? '"+fechado+"'");
            if(fechado.equals("S")){
                check.setSelected(true);
            }else{
                check.setSelected(false);
            }
            jt_especialidade.setText(p.getEspecialidade());
        }
        catch(Exception e){}
      //}catch(Exception e){
      //    JOptionPane.showMessageDialog(null, "Não encontrado");
      //}
      
  }
    
    private void deletar(){
       Prescricao_Controller pc =  new Prescricao_Controller();
       int opcao = JOptionPane.showConfirmDialog(null,"Tem certeza de que deseja excluir essa prescricao?", "Confirmação", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
               
       if(opcao == JOptionPane.YES_OPTION){
            boolean teste = pc.deletar(Integer.parseInt(jt_codigo.getText()));
            if(teste){
                JOptionPane.showMessageDialog(null, "Excluido com sucesso");
                limpar();
            }else{
               // Mensagem_Erro msg = new Mensagem_Erro();
            //    JOptionPane.showMessageDialog(null, "Não é possível excluir, verifique se essa prescricao nao possui itens\n"+msg.getMensagem());
            }
       }
       
       
   }
    private void limpar (){
    jt_codigo.setText("");
    jt_nome.setText("");
    jt_local.setText("");
    jt_prestador.setText("");
    jd_data.setDate(null);
    jt_codigo.requestFocus();
    check.setSelected(false);
    DefaultTableModel tabela = (DefaultTableModel)  table.getModel();
    tabela.setNumRows(0);
}

