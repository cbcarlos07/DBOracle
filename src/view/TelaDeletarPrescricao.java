List lista;
Prescricao_Controller pc = new Prescricao_Controller();

private void buscar_itens(){
    pc = new Prescricao_Controller();
    lista = pc.buscarItens(Long.parseLong(jt_codigo.getText()));
    preencher_Tabela();
}


private void preencher_Tabela(){
    
    DefaultTableModel tabela = (DefaultTableModel) table.getModel();
    tabela.setNumRows(0);
    Iterator i = lista.iterator();
    while (i.hasNext()){
        Item_Prescricao ip = (Item_Prescricao) i.next();
        tabela.addRow(new String[]{
                ""+ip.getCodigo(),
                ip.getDs_item()
        });
    }
    
    int itens = 0;
    try{
        itens = table.getRowCount();
        jl_totalItens.setText(itens+" ITENS PRESCRITOS");
    }catch(Exception e){}
}


