    private void jb_reabrirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jb_reabrirActionPerformed
        if(!(jt_descricao.getText().equals(""))){
            int opcao = JOptionPane.showConfirmDialog(null, "Deseja realmente reabrir essa ordem de serviço?","Confirmação",JOptionPane.YES_NO_OPTION );
            if(opcao == JOptionPane.YES_OPTION){
                alterar();
            } 
            
        }else{
            JOptionPane.showMessageDialog(null, "Por favor informe uma OS válida", "Informa o código",  JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jb_reabrirActionPerformed
private void alterar(){
    Ordem_Servico_Controller osc = new Ordem_Servico_Controller();
    
    boolean teste = osc.alterar(jt_codigo.getText());
    if(teste){
        JOptionPane.showMessageDialog(null, "A Ordem de Serviço foi alterada com sucesso");
    }
}
