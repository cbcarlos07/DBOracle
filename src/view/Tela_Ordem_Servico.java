private void recuperarDados(){
    Ordem_Servico os = new Ordem_Servico();
    Ordem_Servico_Controller osc = new Ordem_Servico_Controller();
    os = osc.pesquisa(jt_codigo.getText());
    try{
         jt_descricao.setText(os.getDescricao());
         jt_responsavel.setText(os.getNmResponsavel());
         String situation = os.getSituacao();
         String sit = "";
         switch(situation){
             case "A":
                  sit = "ABERTA";
                 break;
            case "C":
                  sit = "CONCLUIDA";
                 break;
            case "S":
                  sit = "SOLICITADA";
                 break;   
           case "E":
                  sit = "AGUARDANDO";
                 break;    
           case "F":
                  sit = "PROJETOS FUTUROS";
                 break;        
         }
         
         jt_situacao.setText(sit);
    }catch(Exception e){}
}

